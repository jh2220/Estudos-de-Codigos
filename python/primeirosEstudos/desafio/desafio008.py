# Desenvolva um programa em metros e o exiba convertido em centimetros e milímetros

value = int(input('Digite um valor em metros: '))

centimetro = value * 100
milimitros = value * 1000

print('O valor de {} em centimetros é de {}'.format(centimetro, centimetro))
print('O valor de {} em milimitros é de {}'.format(centimetro, milimitros))